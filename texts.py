def reminder_text(label: str, expiry_str: str, stage: str) -> str:
    if stage == "3d":
        head = f"⏰ Подписка <b>{label}</b> закончится через <b>3 дня</b>."
    elif stage == "1d":
        head = f"⏰ Подписка <b>{label}</b> закончится через <b>1 день</b>."
    else:
        head = f"🔴 Подписка <b>{label}</b> закончилась."
    return (
        f"{head}\n"
        f"Окончание: <b>{expiry_str}</b>\n\n"
        "Нажмите «🔁 Продлить», чтобы отправить заявку администратору."
    )
