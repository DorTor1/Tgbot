        f"Добро пожаловать в <b>Vibecode VPN</b> — быстрый и стабильный доступ без лишней суеты.\n\n"
        f"💰 <b>80 ₽/мес</b> · безлимит устройств · трафика хватает на каждый день\n\n"
        f"⚡️ <b>Важно:</b> включите <b>Split Tunneling</b> в клиенте — VPN только в нужных "
